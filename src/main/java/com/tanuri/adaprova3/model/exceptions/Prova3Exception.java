package com.tanuri.adaprova3.model.exceptions;

public class Prova3Exception extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public Prova3Exception() {
		super();
	}

	public Prova3Exception(String message, Throwable cause) {
		super(message, cause);
	}

	public Prova3Exception(String message) {
		super(message);
	}

	public Prova3Exception(Throwable cause) {
		super(cause);
	}

}
